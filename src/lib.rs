/// returns a 28x28 image of a digit
pub fn mnist_input_image() -> Vec<f32> {
    vec![
        "0000000000000000000000000000",
        "0000000000000000000000000000",
        "0000000000000000000000000000",
        "0000000011111110000000000000",
        "0000000100000011100000000000",
        "0000000000000011110000000000",
        "0000000000000011110000000000",
        "0000000000000011100000000000",
        "0000000000000111000000000000",
        "0000000000001000000000000000",
        "0000000000010000000000000000",
        "0000000000100000000000000000",
        "0000000011000000000000000000",
        "0000000000111000000000000000",
        "0000000000011100000000000000",
        "0000000000000111000000000000",
        "0000000000000001100000000000",
        "0000000000000001100000000000",
        "0000000000000001100000000000",
        "0000000000000011000000000000",
        "0000000000000110000000000000",
        "0000000000011100000000000000",
        "0001111111111000000000000000",
        "0000000000000000000000000000",
        "0000000000000000000000000000",
        "0000000000000000000000000000",
        "0000000000000000000000000000",
        "0000000000000000000000000000",
    ]
    .into_iter()
    .map(|line| {
        line.chars()
            .map(|x| x.to_digit(10).unwrap() as f32)
            .collect::<Vec<_>>()
    })
    .flatten()
    .collect::<Vec<_>>()
}
